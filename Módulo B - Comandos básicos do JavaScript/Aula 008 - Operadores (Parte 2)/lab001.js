// OPERADORES EM JAVASCRIPT (Parte II)

// Operadores relacionais
console.log(5 > 2);
console.log(7 < 4);
console.log(8 >= 8);
console.log(9 <= 7);
console.log(5 == 5);
console.log(4 != 4);
// Note: Os operadores relacionais sempre retornam um valor booleano (true, false) que podem ser armazenados dentro de variávies!

console.log("==============================================");
var resultado = 5 > 2; // true
console.log(resultado);
console.log("==============================================");

/* OPERADORES RELACIONAIS RESUMO:
    > maior
    < menor
    >= maior OU igual
    <= menor OU igual
    == igual
*/

// OPERADOR DE IDENTIDADE
console.log(5 == '5'); // Testa apenas o valor
console.log(5 === 5); // Só retorna 'true' se forem o mesmo valor e o mesmo tipo